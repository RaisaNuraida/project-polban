                                            <li>Sebagai alat evaluasi untuk melihat relevansi antara perguruan tinggi dengan dunia usaha dan industri;</li>
                                            <li>Sebagai masukan bagi perbaikan kinerja dosen dan staf administrasi;</li>
                                            <li>Sebagai masukan bagi perbaikan kurikulum;</li>
                                            <li>Sebagai bahan evaluasi untuk mengikuti akreditasi internasional;</li>
