                                        <h3><strong>Manfaat <i>Tracer Study</i></strong></h3>
                                        <p>Manfaat <i>Tracer Study</i> tidak terbatas pada perguruan tinggi saja, tetapi lebih jauh lagi dapat memberikan informasi penting mengenai hubungan antara dunia pendidikan tinggi dengan dunia usaha dan industri. <i>Tracer Study</i> dapat menyajikan informasi mendalam dan rinci mengenai kecocokan kerja baik horisontal (antar berbagai bidang ilmu) maupun vertikal (antar berbagai level/strata pendidikan). Dengan demikian, <i>Tracer Study</i> dapat ikut membantu mengatasi permasalahan kesenjangan kesempatan kerja dan upaya perbaikannya. Bagi perguruan tinggi, informasi mengenai kompetensi yang relevan bagi dunia usaha dan industri dapat membantu upaya perbaikan kurikulum dan sistem pembelajaran. Di sisi lain, dunia usaha dan industri dapat melihat ke dalam perguruan tinggi melalui <i>Tracer Study</i>, dan dengan demikian dapat menyiapkan diri dengan menyediakan pelatihan-pelatihan yang lebih relevan bagi sarjana pencari kerja baru.</p>
                                        <p><i>Tracer Study</i> dilakukan untuk mendapatkan manfaat sebagai berikut:</p>
