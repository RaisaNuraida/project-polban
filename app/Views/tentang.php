                                        <p><i>Tracer Study</i> atau yang sering disebut sebagai survey alumni atau survey <i>“follow up”</i> adalah studi mengenai lulusan lembaga penyelenggara pendidikan tinggi. Studi ini mampu menyediakan berbagai informasi yang bermanfaat bagi kepentingan evaluasi hasil pendidikan tinggi dan selanjutnya dapat digunakan untuk penyempurnaan dan penjaminan kualitas lembaga pendidikan tinggi yang bersangkutan. <i>Tracer Study</i> juga bermanfaat dalam menyediakan informasi penting mengenai hubungan antara pendidikan tinggi dan dunia kerja professional, menilai relevansi pendidikan tinggi, informasi bagi pemangku kepentingan <i>(stakeholders)</i>, dan kelengkapan persyaratan bagi akreditasi pendidikan tinggi.</p>
                                        <p>Perguruan tinggi perlu melaksanakan <i>Tracer Study</i> karena membutuhkan umpan balik dari alumni dalam usahanya untuk perbaikan sistem dan pengelolaan pendidikan. Perguruan tinggi di awal tahun ajaran menentukan arah kebijakan pendidikan tinggi dari masukkan berupa kondisi, pengalaman, dan motivasi mahasiswa baru yang masuk ke perguruan tinggi tersebut. Masukkan mengenai kondisi, pengalaman dan motivasi ini menentukan pula perguruan tinggi dalam menerapkan sistem dan pengelolaan pendidikan dalam hal pola/proses pengajaran dan pembelajaran, penelitian, praktikum, workshop, laboratorium, studio ataupun riset. Penerapan sistem pengajaran dan pembelajaran inipun akan dipengaruhi pula oleh kebijakan pendidikan yang ditetapkan oleh perguruan tinggi.</p>
                                        <p>Hasil dari masukkan berupa kondisi, pengalaman dan motivasi mahasiswa, sistem dan kebijakan pendidikan di perguruan tinggi, dan proses pengajaran dan pembelajaran di perguruan tinggi akan membantu dalam membentuk karakter/kompetensi dari lulusan perguruan tinggi itu sendiri. Lulusan/alumni dari perguruan tinggi umumnya akan memiliki pengetahuan, kemampuan, motivasi dan kompetensi yang dibutuhkan untuk memasuki dunia kerja</p>
                                        <p>Hasil dari pendidikan tinggi adalah pengetahuan, kemampuan dan kompetensi alumni perguruan tinggi yang dibutuhkan untuk memasuki dunia kerja. Hasil-hasil ini beserta kondisi saat alumni menjalani pekerjaan di awal karir mereka merupakan hal-hal yang dibutuhkan bagi perguruan tinggi untuk perbaikan sistem dan pengelolaan pendidikan. Kebutuhan untuk mengetahui rekam jejak alumni serta hubungan pendidikan tinggi dengan pekerjaan inilah yang menjadi konsep dasar dalam penelitian <i>Tracer Study</i>.</p>
                                        <p>Pada tahun 2016 Politeknik Negeri Bandung telah melakukan <i>Tracer Study</i> untuk mahasiswa yang lulus pada tahun 2014. <i>Tracer Study</i> ini dilakukan secara online. Penyebaran informasi kepada alumni tentang alamat tautan untuk pengisian data dilakukan melalui email dan sms. Selanjutnya, alumni membuka alamat tautan tersebut, mengisi data yang diminta dan mengirimkan kuesioner yang telah diisi. Kuesioner berisi pertanyaan tentang data pribadi, penelusuran pekerjaan pertama dan saat ini, serta pertanyaan-pertanyaan seputar proses belajar mengajar semasa kuliah di Polban.</p>
